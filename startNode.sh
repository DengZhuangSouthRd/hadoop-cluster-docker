#!/bin/bash

CURPATH=`pwd`
# domain for containers
CONTAINER_DOMAIN=phoenix.duc.com
CONTAINER_HOSTS=${CURPATH}/docker-container-hosts

# find the dns IP address
findDockerDnsIP() {
    dnsip=`ip addr show dev docker0 | grep 'inet ' | awk -F' ' '{ print $2  }' | awk -F'/' '{ print $1  }'`
    eval "$1=${dnsip}"
}

autoCreateHostsFile() {
    echo "# auto generated by $0" > ${CONTAINER_HOSTS}
    for CID in `sudo docker ps -q`;do
        IP=`sudo docker inspect --format '{{ .NetworkSettings.IPAddress }}' ${CID}`

        NAME=`sudo docker inspect --format '{{ .Config.Hostname }}' ${CID}`
        echo "${IP} ${NAME}.${CONTAINER_DOMAIN}" >> ${CONTAINER_HOSTS}
    done
    sudo pkill -x -HUP dnsmasq
}

autoStartDockerSlaveImage() {
    NUM=$1
    DNSIP=$2
    i=1
    while [ $i -lt $NUM ]
    do
        sudo docker run -itd \
            --privileged \
            --dns ${DNSIP} \
            --name slave$i \
            --hostname slave$i \
            liuguiyangnwpu/ubuntu:basic 
        i=$(( $i + 1 ))
    done 
}

autoStartDockerMasterImage() {
    DNSIP=$1
    sudo docker run -itd \
        --privileged \
        --dns ${DNSIP} \
        --name master \
        --hostname master \
        liuguiyangnwpu/ubuntu:basic 
}

autoStartDockerAmbariImage() {
    DNSIP=$1
    sudo docker run -itd \
        --dns ${DNSIP} \
        --name ambari \
        --hostname ambari \
        -p 8088:8080 \
        liuguiyangnwpu/ubuntu:ambari01
    sleep 1
}

sendRsaPub2DockerSlave() {
    NUM=$1
    for i in `seq $((${NUM}-1))`
    do
        sudo docker exec slave${i} cat ${CURPATH}/id_rsa.pub > /root/.ssh/authorized_keys
    done
    sudo docker exec master cat ${CURPATH}/docker-container-hosts >> /etc/hosts
    sudo docker exec ambari cat ${CURPATH}/docker-container-hosts >> /etc/hosts
}

DNSIP='127.0.0.1'
findDockerDnsIP DNSIP
sudo docker rm -f `sudo docker ps -a -q`
autoStartDockerMasterImage ${DNSIP}
autoStartDockerSlaveImage 6 ${DNSIP}
autoStartDockerAmbariImage ${DNSIP}
autoCreateHostsFile
sendRsaPub2DockerSlave 6
sudo docker exec -it ambari /bin/bash
